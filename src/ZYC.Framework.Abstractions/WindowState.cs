namespace ZYC.Framework.Abstractions;

/// <summary>
///     Represents possible window states.
/// </summary>
public enum WindowState
{
    /// <summary>
    ///     The window is restored.
    /// </summary>
    Normal,

    /// <summary>
    ///     The window is minimized.
    /// </summary>
    Minimized,

    /// <summary>
    ///     The window is maximized.
    /// </summary>
    Maximized
}